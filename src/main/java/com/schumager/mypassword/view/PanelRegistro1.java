    public boolean isResgistro(){
        if(textFieldUrl.getText().equals("")){
            return false;
        }
        if(textFieldNombre.getText().equals("")){
            return false;
        }
        if(textFieldMail.getText().equals("")){
            return false;
        }
        if(textFieldUser.getText().equals("")){
            return false;
        }
        if(textFieldPassword.getText().equals("")){
            return false;
        }
        return true;
    }
